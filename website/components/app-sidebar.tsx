export function AppSidebar({ ...props }: React.ComponentProps<typeof Sidebar>) {
  const { me, setMe } = useMe()
  const role = me?.role ?? null

  const navMain = React.useMemo(() => {
    const items = [...data.navMain]
    if (role === "admin" || role === "super_admin") {
      items.push({ title: "LLM", url: "/dashboard/llm", icon: IconFileAi })
      items.push({ title: "Users", url: "/dashboard/users", icon: IconUsers })
    }
    return items
  }, [role])

  return (
    <Sidebar collapsible="offcanvas" {...props}>
      <SidebarHeader>
        <SidebarMenu>
          <SidebarMenuItem>
            <SidebarMenuButton
              asChild
              className="data-[slot=sidebar-menu-button]:p-1.5!"
            >
              <a href="#">
                <IconInnerShadowTop className="size-5!" />
                <span className="text-base font-semibold">LLM Studio</span>
              </a>
            </SidebarMenuButton>
          </SidebarMenuItem>
        </SidebarMenu>
      </SidebarHeader>
      <SidebarContent>
        <NavMain items={navMain} />
        <NavDocuments items={data.documents} />
        <NavSecondary items={data.navSecondary} className="mt-auto" />
      </SidebarContent>
      <SidebarFooter>
        <NavUser me={me as Me | null} onMeUpdated={setMe} />
      </SidebarFooter>
    </Sidebar>
  )
}
